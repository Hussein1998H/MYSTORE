        #region delete 
        private void btndelete_Click(object sender, EventArgs e)
        {
            try
            {
                id = Convert.ToInt32(gridView1.GetFocusedRowCellValue("Cust_ID"));
                var res = MessageBox.Show("هل أنت متأكد", "Delete", MessageBoxButtons.YesNo, MessageBoxIcon.Warning);
                if (res == DialogResult.Yes)
                {
                    mycustomer = db.Customers.Where(x => x.Cust_ID == id).FirstOrDefault();
                    db.Entry(mycustomer).State = EntityState.Deleted;
                    db.SaveChanges();
                    grid_cust.DataSource = db.Customers.ToList();
                    claredata();
                    frm_Notivecation note = new frm_Notivecation();
                    note.lab_message.Text = "تم حذف البيانات بنجاح";
                    note.Show();
                }
            }
            catch (Exception ex)
            {

                MessageBox.Show(ex.Message);
            }
        }
