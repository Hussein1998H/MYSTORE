        #region add 

        private void btnadd_Click(object sender, EventArgs e)
        {


            if (txtname.Text == "" || txtcomp.Text == "" || txtaddress.Text == "" || txtphone.Text == "" || txtmail.Text == "" || txtaccount.Text == "")
            {
                MessageBox.Show(" ! الرجاء إدخال جميع البيانات", "Missing Data", MessageBoxButtons.OK, MessageBoxIcon.Warning);
                pnl_c_top.Height = 275;
                btn_colaps.Location = new Point(0, 250);
                txtname.Focus();
            }
            else
            {
                Regex reg = new Regex(@"^(\d+)|(\d+\.\d+)$");
                Regex reg1 = new Regex(@"^\+?\d+$");
                if ((!reg.IsMatch(txtaccount.Text)) || (!reg1.IsMatch(txtphone.Text)))
                {
                    MessageBox.Show("! الرجاء إدخال أرقام فقط");
                }
                else
                {
                    if (db.Customers.Any(x => x.Name == txtname.Text && x.CompanyName == txtcomp.Text))
                    {
                        var res = MessageBox.Show("هذا الزبون موجود مسبقاً", "Warring", MessageBoxButtons.OK, MessageBoxIcon.Warning);
                        grid_cust.DataSource = db.Customers.Where(y => y.Name == txtname.Text).ToList();
                    }
                    else
                    {

                        try
                        {
                            frm_Notivecation note = new frm_Notivecation();
                            mycustomer.Name = txtname.Text;
                            mycustomer.CompanyName = txtcomp.Text;
                            mycustomer.Address = txtaddress.Text;
                            mycustomer.Phone = txtphone.Text;
                            mycustomer.Mail = txtmail.Text;
                            mycustomer.Account = double.Parse(txtaccount.Text);
                            db.Customers.Add(mycustomer);
                            db.SaveChanges();
                            claredata();
                            txtname.Focus();
                            grid_cust.DataSource = db.Customers.ToList();
                            note.lab_message.Text = "تمت إضافة بيانات الزبون بنجاح";
                            note.Show();
                        }
                        catch (Exception)
                        {

                            throw;
                        }
                    }
                }
            }
        }
